#include <avr/io.h>

class FastSerial {
public:
    // Инициализация Serial (замена Serial.begin)
    // inline позволяет компилятору вшить настройку прямо в setup()
    static inline void begin(unsigned long baudrate) {
        // Рассчитываем значение для регистра скорости (UBRR) исходя из частоты 16 МГц
        uint16_t ubrr = (16000000UL / (16UL * baudrate)) - 1;
        
        UBRR0H = (ubrr >> 8); // Записываем старший байт скорости
        UBRR0L = ubrr;        // Записываем младший байт скорости
        
        UCSR0B |= (1 << TXEN0); // Включаем только передатчик (TX)
        // Если нужен прием (RX), добавили бы: | (1 << RXEN0)
        
        UCSR0C = (1 << UCSZ01) | (1 << UCSZ00); // Режим работы: 8 бит данных, 1 стоп-бит
    }

    // Отправка одного символа (байта)
    static inline void write(char c) {
        // Ждем, пока очистится буфер передачи (флаг UDRE0 в регистре UCSR0A станет равен 1)
        while (!(UCSR0A & (1 << UDRE0))); 
        
        // Как только буфер пуст — кидаем байт в регистр данных UDR0
        UDR0 = c; 
    }

    // Отправка строки (массива символов)
    static void print(const char* str) {
        // Перебираем строку по указателю, пока не встретим нуль-терминатор '\0'
        while (*str) {
            write(*str);
            str++;
        }
    }

    // Шаблон для вывода любых целочисленных типов
    template <typename T>
    static void print(T num) {
        // Если число отрицательное, выводим минус и делаем число положительным
        if (num < 0) {
            write('-');
            num = -num;
        }

        // Если пришел чистый 0, то цикл ниже не сработает, обрабатываем отдельно
        if (num == 0) {
            write('0');
            return;
        }

        char buffer[20]; // Буфер для хранения цифр (для 64-битных чисел максимум 20 знаков)
        uint8_t i = 0;

        // Разбираем число на цифры с конца
        while (num > 0) {
            buffer[i++] = (num % 10) + '0'; // Получаем символ цифры и кладем в буфер
            num /= 10;
        }

        // Выводим цифры из буфера в обратном порядке (теперь они будут слева направо)
        while (i > 0) {
            write(buffer[--i]);
        }
    }

    // Отправка строки с переносом каретки (замена println)
    static inline void println(const char* str) {
        print(str);
        write('\r');
        write('\n');
    }
};